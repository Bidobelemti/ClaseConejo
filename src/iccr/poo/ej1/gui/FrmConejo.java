    /*metodo recorre todo el arreglo y colocar cada elemento*/
    private void llenarTabla() {
        /*colocar el nombre, peso y edad de cada elemento del arreglo 
        conejos en una fila de la tabla tblConejos
         */

        for (Conejo c : conejos) {
            dtmModelo.addRow(new Object[]{c.getNombre(), c.getPeso(), c.getEdad()});
        }
    }

    private void agregarFila() {
        dtmModelo.addRow(new Object[]{c.getNombre(), c.getPeso(), c.getEdad()});

    }


