    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            int edad = Integer.parseInt(txtEdad.getText());
            double peso = Double.parseDouble(txtPeso.getText());
            double tasaCrecimientoPorcentual = Double.parseDouble(txtTasaCrecimientoPorcentual.getText());
            String nombre = txtNombre.getText();

            c = new Conejo(edad, peso, tasaCrecimientoPorcentual, nombre);

            String raza = cmbRaza.getSelectedItem().toString();
            c.setRaza(raza);
            /* Asignar el valor de la variable sexo
        dependiendo de la selecci√≥n del radio button
             */
            if (rbtFemenino.isSelected()) {
                c.setSexo("FEMENINO");
            }

            if (rbtMasculino.isSelected()) {
                c.setSexo(rbtMasculino.getText().toUpperCase());
            }

            /*
        asignar el valor de true en la variable estaEsterilizado
        cuando el check box se encuentre seleccionado
             */
            if (chkEsterilizado.isSelected()) {
                c.setEstaEsterilizado(true);
            }

            txtaResultado.append(c.toString() + "\n");
            //Colocar la instancia creada en el arreglo
            //utilizar un contador 
            conejos[contador] = c;
            contador++;
            agregarFila();

            //botones activados
            btnGuardarDieta.setEnabled(true);
            btnDiagnosticar.setEnabled(true);
            btnNuevo.setEnabled(true);

            JOptionPane.showMessageDialog(rootPane, "Conejo "
                    + "registrado exitosamente", "Informacion", 1);

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null, "Debe ingresar numeros "
                    + "en la edad, peso y tasa de crecimiento", "Error",
                    JOptionPane.ERROR_MESSAGE);

        } catch (ArrayIndexOutOfBoundsException aiobe) {
            JOptionPane.showMessageDialog(null, "El arreglo no puede "
                    + "guadar mas de " + TAMANIO_ARREGLOS + " elementos",
                    "Elementos", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed


