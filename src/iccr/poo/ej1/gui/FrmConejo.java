    private void btnGuardarDietaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarDietaActionPerformed
        // TODO add your handling code here:
        //alamacenar cada una de las opciones seleccionadas en una posicion del 
        //arreglo dieta
        String dietaSeleccionada[] = new String[5];
        if (chkAlfalfa.isSelected()) {
            dietaSeleccionada[0] = "Alfalfa";
        }

        if (chkHeno.isSelected()) {
            dietaSeleccionada[1] = chkHeno.getText();
        }

        if (chkAlimentoProcesado.isSelected()) {
            dietaSeleccionada[2] = chkAlimentoProcesado.getText();
        }

        if (chkZanahoria.isSelected()) {
            dietaSeleccionada[3] = chkZanahoria.getText();
        }

        if (chkPlatano.isSelected()) {
            dietaSeleccionada[4] = chkPlatano.getText();
        }

        c.setDieta(dietaSeleccionada);

        JOptionPane.showMessageDialog(rootPane, c.presentarDieta(),
                "Informacion", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnGuardarDietaActionPerformed

